// comparison operators

let x = 1;

// strict equality operator (have same type and value)
console.log(1 === 1);
console.log('1' === 1);
console.log(true === 1);
console.log();

// lose equality operator (have same value)
console.log(1 == 1);
console.log('1' == 1);
console.log(true == 1); // converts 1 to boolean
                        // and compares true to true
