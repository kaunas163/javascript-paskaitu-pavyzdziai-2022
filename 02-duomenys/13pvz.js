// dynamic typing
// programming languages can be static or dynamic
// javascript is dynamic language
// in static languages when variable is created it
// cannot change its type
// but in dynamic languages variables can change their
// type at runtime

let vardas = 'Greta';
let amzius = 22;
let studente = true;
let teises = undefined;
let augintinis = null;

console.log(typeof vardas);
console.log(typeof amzius);
console.log(typeof studente);
console.log(typeof teises);
console.log(typeof augintinis);
